#!/usr/bin/env node
import fs from 'fs/promises';
import path from 'path';
import {
  cdnQuestionShowcaseStatsPath,
  cdnQuestionTagRegistryPath,
  cdnQuestionTopicRegistryPath,
  cdnQuestionTrackRegistryPath,
  generatedAppDir,
  relFromFrontend,
} from './content-paths.mjs';

const OUTPUT_PATH = path.join(generatedAppDir, 'content-metadata.ts');

async function readJson(filePath) {
  const raw = await fs.readFile(filePath, 'utf8');
  return JSON.parse(raw);
}

async function main() {
  const [trackRegistry, topicRegistry, tagRegistry, showcaseStats] = await Promise.all([
    readJson(cdnQuestionTrackRegistryPath),
    readJson(cdnQuestionTopicRegistryPath),
    readJson(cdnQuestionTagRegistryPath),
    readJson(cdnQuestionShowcaseStatsPath),
  ]);

  const source = `// Auto-generated by scripts/gen-content-metadata.mjs. Do not edit by hand.
export const TRACK_REGISTRY = ${JSON.stringify(trackRegistry, null, 2)};

export const TOPIC_REGISTRY = ${JSON.stringify(topicRegistry, null, 2)};

export const TAG_REGISTRY = ${JSON.stringify(tagRegistry, null, 2)};

export const SHOWCASE_STATS = ${JSON.stringify(showcaseStats, null, 2)};
`;

  await fs.mkdir(path.dirname(OUTPUT_PATH), { recursive: true });
  await fs.writeFile(OUTPUT_PATH, source, 'utf8');
  console.log(`[gen-content-metadata] wrote ${relFromFrontend(OUTPUT_PATH)}`);
}

main().catch((error) => {
  console.error('[gen-content-metadata] fatal:', error);
  process.exit(1);
});
